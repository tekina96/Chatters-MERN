import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
        minlength: 6,
    },
    gender: {
        type: String,
        required: true,
        enum: ["male", "female"],
    },
    profilePic: {
        type: String,
        default: "",
    },
}, {timestamps: true});   
// CreatedAt, updatedAt => Member since <createdAt>

const User = mongoose.model("User", userSchema);
// here we must use it like this with first letter being in capital

export default User;